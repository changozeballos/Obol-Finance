import type { Section } from '../../types';

const mc = (
  id: string,
  textKey: string,
  options: [string, string, string, string],
  correctIdx: 0 | 1 | 2 | 3,
  explanationKey: string,
  group?: 1 | 2 | 3 | 4,
) => ({
  id,
  type: 'multiple_choice' as const,
  textKey,
  options: [
    { id: 'a', textKey: `${textKey}_a` },
    { id: 'b', textKey: `${textKey}_b` },
    { id: 'c', textKey: `${textKey}_c` },
    { id: 'd', textKey: `${textKey}_d` },
  ],
  correctId: ['a', 'b', 'c', 'd'][correctIdx],
  explanationKey,
  ...(group ? { group } : {}),
});

const tf = (
  id: string,
  textKey: string,
  correct: 'true' | 'false',
  explanationKey: string,
  group?: 1 | 2 | 3 | 4,
) => ({
  id,
  type: 'true_false' as const,
  textKey,
  options: [
    { id: 'true', textKey: 'q.true' },
    { id: 'false', textKey: 'q.false' },
  ],
  correctId: correct,
  explanationKey,
  ...(group ? { group } : {}),
});

// ── Tipos de preguntas interactivas ──────────────────────────────────────────
const classify = (
  id: string,
  textKey: string,
  items: { id: string; labelKey: string; bucket: string }[],
  buckets: { id: string; labelKey: string }[],
  explanationKey: string,
) => ({ id, type: 'classify' as const, textKey, items, buckets, explanationKey });

const match_q = (
  id: string,
  textKey: string,
  pairs: { leftKey: string; rightKey: string }[],
  explanationKey: string,
) => ({ id, type: 'match' as const, textKey, pairs, explanationKey });

const fill_num = (
  id: string,
  textKey: string,
  correctIdx: 0 | 1 | 2 | 3,
  explanationKey: string,
) => ({
  id, type: 'fill_number' as const, textKey,
  options: [
    { id: 'a', textKey: `${textKey}_a` },
    { id: 'b', textKey: `${textKey}_b` },
    { id: 'c', textKey: `${textKey}_c` },
    { id: 'd', textKey: `${textKey}_d` },
  ],
  correctId: ['a', 'b', 'c', 'd'][correctIdx],
  explanationKey,
});

const graph_pt = (
  id: string,
  textKey: string,
  labels: string[],
  values: number[],
  correctLabel: string,
  explanationKey: string,
) => ({ id, type: 'graph_point' as const, textKey, labels, values, correctLabel, explanationKey });

const order_q = (
  id: string,
  textKey: string,
  items: { id: string; labelKey: string; position: number }[],
  explanationKey: string,
) => ({ id, type: 'order' as const, textKey, items, explanationKey });

const graph_id = (
  id: string,
  textKey: string,
  correctIdx: 0 | 1 | 2 | 3,
  series: { values: number[]; color?: string }[],
  labels: string[],
  explanationKey: string,
) => ({
  id, type: 'graph_id' as const, textKey,
  options: [
    { id: 'a', textKey: `${textKey}_a` },
    { id: 'b', textKey: `${textKey}_b` },
    { id: 'c', textKey: `${textKey}_c` },
    { id: 'd', textKey: `${textKey}_d` },
  ],
  correctId: ['a', 'b', 'c', 'd'][correctIdx],
  series, labels, explanationKey,
});

const slider_q = (
  id: string,
  textKey: string,
  correct: number,
  tolerance: number,
  min: number,
  max: number,
  unit: string,
  explanationKey: string,
) => ({ id, type: 'slider' as const, textKey, correct, tolerance, min, max, unit, explanationKey });

